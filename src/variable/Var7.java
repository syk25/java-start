package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 10; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불린
        char d = 'A'; // 문자
        String e = "abcde"; // 문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
