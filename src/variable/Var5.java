package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 1개 선언 후 + 별도 초기화
        int a;
        a = 10;
        System.out.println(a);

        // 변수의 선언과 초기화 동시에
        int b = 10;
        System.out.println(b);

        // 여러 변수 초기화도 가능
        int c = 10, d = 20;
        System.out.println(c);
        System.out.println(d);

    }
}
